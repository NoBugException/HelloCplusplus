#include "Ls_03_02.h"

void Ls_03_02::printChar()
{
    int x = 123456;
    printf("12与8的结果：%d\n", (12 & 8));    // 位逻辑与计算整数的结果
    printf("4或8的结果：%d\n", (4 | 8));      // 位逻辑或计算整数的结果
    printf("31异或22的结果：%d\n", (31 ^ 22));    // 位逻辑异或计算整数的结果
    printf("123取反的结果：%d\n", ~x);        // 位逻辑取反计算整数的结果
    // 位逻辑与计算布尔值的结果
    printf("2>3与4!=7的与结果：%d\n", (2 > 3 & 4 != 7));
    // 位逻辑或计算布尔值的结果
    printf("2>3与4!=7的或结果：%d\n", (2 > 3 | 4 != 7));
    // 位逻辑异或计算布尔值的结果
    printf("2<3与4!=7的与异或结果：%d\n", (2 < 3 ^ 4 != 7));
}
